namespace CourseRegistration.Domain.Enums;

/// <summary>
/// Represents the grade assigned to a student for a course
/// </summary>
public enum Grade
{
    /// <summary>
    /// Grade A (Excellent)
    /// </summary>
    A = 0,

    /// <summary>
    /// Grade B (Good)
    /// </summary>
    B = 1,

    /// <summary>
    /// Grade C (Satisfactory)
    /// </summary>
    C = 2,

    /// <summary>
    /// Grade D (Poor)
    /// </summary>
    D = 3,

    /// <summary>
    /// Grade F (Fail)
    /// </summary>
    F = 4
}